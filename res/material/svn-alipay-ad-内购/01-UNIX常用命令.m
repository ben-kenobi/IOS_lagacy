FSVN附录：UNIX常用命令
================================================================================
1.  cd      改变工作目录
    cd ..   返回上一层目录

2.  pwd     输出当前工作目录的绝对路径
在UNIX中要执行什么命令，一定要知道自己当前所在的工作目录

3.  ls      查看文件
$ ls        显示文件
$ ls -a     显示所有文件 包括隐藏文件
$ ls -l     列表显示文件
$ ls -la    列表显示所有文件 包括隐藏文件

4.  touch   用于更改文件访问和修改时间的标准UNIX程序，也被用于创建新文件

$ touch test.txt

注意：touch不修改test.txt内容，只更改它的访问、修改时间，如果test.txt不存在，它会被创建

5.  cat     连续查看文件内容

6.  more    分页查看文件内容   F下一页 B上一页

7. open 打开文件

提示：

1>  命令和参数之间需要添加空格
2>  如果要使用当前目录中的文件名，输入到一半时，按TAB键能够补全

